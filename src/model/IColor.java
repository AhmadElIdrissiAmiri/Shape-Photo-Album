package model;

/**
 * The interface Color.
 */
public interface IColor {

  /**
   * Gets r.
   *
   * @return the r
   */
  double getR();

  /**
   * Gets g.
   *
   * @return the g
   */
  double getG();

  /**
   * Gets b.
   *
   * @return the b
   */
  double getB();

  /**
   * toString method.
   *
   * @return String.
   */
  String toString();

}
